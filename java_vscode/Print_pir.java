public class Print_pir {
    public static void main(String[] args) {
        int k=1,i,j;
        for(i=0;i<5;i++)
        {
            for(j=0;j<=i;j++)
            {
                System.out.print("  "+k);
                k++;
            }
            System.out.println("");
        }
    }

    
}
